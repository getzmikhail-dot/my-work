_VACANCY_SCALE = 200   # число вакансий, при котором рыночный балл = 1.0
_MARKET_FACTOR_MIN = 0.7   # минимальный множитель при отсутствии вакансий
_MARKET_FACTOR_MAX = 1.15  # максимальный множитель при насыщенном рынке
_SMOOTH_PRIOR_N = 10       # «вес» априорного значения при байесовском сглаживании


def _vacancy_score(count: int) -> float:
    """Нормализует число вакансий в [0, 1] через логарифм."""
    if count <= 0:
        return 0.0
    return min(1.0, _math.log(count + 1) / _math.log(_VACANCY_SCALE + 1))


def _market_factor(count: int) -> float:
    """Мягкий множитель рынка ∈ [_MARKET_FACTOR_MIN, _MARKET_FACTOR_MAX].
    0 вакансий → 0.7 (понижаем), много вакансий → ~1.15 (немного повышаем).
    """
    s = _vacancy_score(count)
    return _MARKET_FACTOR_MIN + (_MARKET_FACTOR_MAX - _MARKET_FACTOR_MIN) * s


def _smooth(p_model: float, p_prior: float, coverage: int) -> float:
    """Байесовское сглаживание: при малом coverage тянем к p_prior.
    При coverage >> _SMOOTH_PRIOR_N → p_model; при coverage=0 → p_prior.
    """
    if coverage < 0:
        coverage = 0
    w_model = coverage / (coverage + _SMOOTH_PRIOR_N)
    return w_model * p_model + (1.0 - w_model) * p_prior


# Усиленный рыночный score: насыщение на 500 вакансий, шкала [0, 1].
# Используется как полноценный второй источник вероятности, а не мягкий множитель.
_MARKET_SCALE_HARD = 500


def _market_score_strong(count: int) -> float:
    if count <= 0:
        return 0.0
    return min(1.0, _math.log(count + 1) / _math.log(_MARKET_SCALE_HARD + 1))


# Минимальное число записей в выборке, при котором уровень иерархии prior'а
# считается «активированным». Иначе пробуем более общий уровень.
_PRIOR_MIN_SUPPORT = 10


def _hierarchical_employment_prior(
    features: Dict[str, Any],
    ignored: set,
) -> tuple:
    """Возвращает (prior, level_name, support) — самый специфичный prior P(employed),
    подкреплённый хотя бы _PRIOR_MIN_SUPPORT записями в выборке.

    Уровни иерархии (от специфичного к общему):
      L0: direction + education_level + city
      L1: direction + education_level
      L2: direction
      L3: education_level
      L4: global
    """
    if _stored_X is None or _stored_y is None or len(_stored_y) == 0:
        return 0.5, 'unavailable', 0

    # Локальные ссылки — чтобы type checker видел non-None внутри nested-функций.
    y = _stored_y
    X = _stored_X

    def _f(col: str):
        if col in ignored or col not in features:
            return None
        v = features.get(col)
        if v is None or str(v).strip() == '' or str(v).strip().lower() == 'не указано':
            return None
        return str(v)

    direction = _f('direction')
    edu = _f('education_level')
    city = _f('city')

    def _prior_for_mask(mask: pd.Series) -> tuple:
        n = int(mask.sum())
        if n < _PRIOR_MIN_SUPPORT:
            return None, n
        emp = float((y[mask] > 0).mean())
        return emp, n

    levels: List[tuple] = []
    if direction and edu and city and all(c in X.columns for c in ('direction', 'education_level', 'city')):
        m = (X['direction'].astype(str) == direction) & \
            (X['education_level'].astype(str) == edu) & \
            (X['city'].astype(str) == city)
        levels.append(('direction+edu+city', m))
    if direction and edu and all(c in X.columns for c in ('direction', 'education_level')):
        m = (X['direction'].astype(str) == direction) & (X['education_level'].astype(str) == edu)
        levels.append(('direction+edu', m))
    if direction and 'direction' in X.columns:
        levels.append(('direction', X['direction'].astype(str) == direction))
    if edu and 'education_level' in X.columns:
        levels.append(('edu', X['education_level'].astype(str) == edu))

    for name, mask in levels:
        p, n = _prior_for_mask(mask)
        if p is not None:
            return p, name, n

    # Глобальный — всегда есть
    n_total = int(len(y))
    n_emp = int((y > 0).sum())
    return (n_emp / n_total if n_total else 0.5), 'global', n_total


def _compute_shap(model_name: str, row: pd.DataFrame) -> Optional[Dict[str, Any]]:
    """Возвращает локальные SHAP-значения для one-row DataFrame.

    Возвращает dict вида:
        {'base': float, 'features': [{'name': str, 'feature_value': str, 'shap': float}, ...]}
    или None, если SHAP недоступен (библиотека не установлена или модель не обучена).

    Объясняется ВЕРОЯТНОСТЬ КЛАССА «трудоустроен по специальности» (y == 2).
    Используется shap.KernelExplainer (универсальный, корректно работает со строковыми
    категориальными признаками). Кэшируется по имени модели в _shap_explainers.
    """
    global _shap_explainers
    if model_name not in _trained:
        print(f'[shap] модель {model_name!r} не обучена')
        return None
    if _shap_background is None or len(_shap_background) == 0:
        print('[shap] фоновая выборка пуста — переобучите модели')
        return None
    # Локальная копия — чтобы pyright не ругался на возможный None в замыкании.
    bg_df = _shap_background
    try:
        import shap  # type: ignore[import-not-found]
    except ImportError:
        print('[shap] библиотека shap не установлена')
        return None
    pipe = _trained[model_name]
    try:
        classes = list(pipe.classes_)
        if 2 not in classes:
            print(f'[shap] класс 2 (по специальности) отсутствует в {model_name}')
            return None
        idx2 = classes.index(2)

        # Приводим row к тем же колонкам/типам, что и bg_df, иначе KernelExplainer
        # может прислать numpy-массив с object-dtype для столбца, который в фоне
        # был int — pipe сломается на ColumnTransformer.
        bg_cols = list(bg_df.columns)
        row_aligned = row.reindex(columns=bg_cols)
        for c in bg_cols:
            try:
                row_aligned[c] = row_aligned[c].astype(bg_df[c].dtype)
            except Exception:
                pass  # если dtype не сходится, оставим как есть — pipe сам ругнётся

        # predict-функция: P(y=2) на массиве строк-признаков.
        # KernelExplainer передаёт numpy-массив; восстановим DataFrame.
        def _predict_proba_spec(X):
            if isinstance(X, np.ndarray):
                X = pd.DataFrame(X, columns=bg_cols)
                for c in bg_cols:
                    try:
                        X[c] = X[c].astype(bg_df[c].dtype)
                    except Exception:
                        pass
            return pipe.predict_proba(X)[:, idx2]

        explainer = _shap_explainers.get(model_name)
        if explainer is None:
            # KernelExplainer устойчив к категориальным строкам.
            explainer = shap.KernelExplainer(_predict_proba_spec, bg_df)
            _shap_explainers[model_name] = explainer

        # Объясняем единственную строку. nsamples — компромисс скорости и точности.
        # Снизили с 80 → 50 для скорости (на Render Free 0.1 CPU 80 семплов могут
        # давать таймаут > 30 с на первый запрос).
        sv = explainer.shap_values(row_aligned, nsamples=50, silent=True)
        # sv может быть np.ndarray shape (1, n_features) для бинарного выхода.
        # Если вдруг list — берём первый элемент.
        if isinstance(sv, list):
            sv = sv[0]
        sv = np.asarray(sv)
        if sv.ndim == 2:
            vals = sv[0]
        else:
            vals = sv
        # expected_value в новых версиях shap может быть np.ndarray (например,
        # shape (1,)) даже для одно-выходной функции. float() на массиве — TypeError.
        ev = explainer.expected_value
        ev_arr = np.atleast_1d(np.asarray(ev))
        base = float(ev_arr[0])
        cols = list(row_aligned.columns)
        feats = [
            {
                'name': str(c),
                'feature_value': str(row_aligned.iloc[0][c]),
                'shap': float(vals[i]),
            }
            for i, c in enumerate(cols)
        ]
        # Сортировка по убыванию модуля вклада — самые влиятельные первыми.
        feats.sort(key=lambda f: abs(f['shap']), reverse=True)
        return {'base': base, 'features': feats}
    except Exception as e:
        # Полный трейсбек в server log — без него не понять, где упало.
        print(f'[shap] _compute_shap failed for {model_name}: {e}')
        _tb.print_exc()
        return None


def _compute_lime(model_name: str, row: pd.DataFrame) -> Optional[Dict[str, Any]]:
    """Возвращает локальные LIME-значения для one-row DataFrame.

    Возвращает dict вида:
        {'base': float, 'features': [{'name': str, 'feature_value': str, 'lime': float}, ...]}
    или None, если LIME недоступен или модель не обучена.

    Базовое значение base — глобальная средняя предсказанная вероятность класса 2
    по фоновой выборке X_ml (та же, что используется в LimeTabularExplainer).
    """
    if model_name not in _trained or _lime_explainer is None or not _lime_meta:
        return None
    pipe = _trained[model_name]
    try:
        classes = list(pipe.classes_)
        if 2 not in classes:
            return None
        idx2 = classes.index(2)
        cols: List[str] = _lime_meta['cols']
        cat_value_to_idx: Dict[str, Dict[str, int]] = _lime_meta['cat_value_to_idx']

        # Закодировать строковые значения текущей строки в числовой вектор
        # тем же индексным маппингом, что использовался при обучении LIME.
        numeric_row = np.zeros(len(cols), dtype=float)
        for j, col in enumerate(cols):
            val = str(row.iloc[0][col])
            numeric_row[j] = cat_value_to_idx.get(col, {}).get(val, 0)

        # predict_fn для LIME: получает закодированную матрицу, восстанавливает
        # строковую DataFrame и вызывает predict_proba исходного pipeline.
        def _predict_fn(X_num: np.ndarray) -> np.ndarray:
            rows = []
            for i in range(X_num.shape[0]):
                rec = {}
                for j, col in enumerate(cols):
                    inv_map = _lime_meta['categorical_names'][j]
                    code = int(X_num[i, j])
                    code = max(0, min(code, len(inv_map) - 1))
                    rec[col] = inv_map[code]
                rows.append(rec)
            df = pd.DataFrame(rows, columns=cols)
            return pipe.predict_proba(df)

        # Запросить LIME-объяснение — ограничиваем num_samples ради скорости.
        exp = _lime_explainer.explain_instance(
            numeric_row, _predict_fn,
            num_features=len(cols), num_samples=300, labels=(idx2,),
        )
        contrib = dict(exp.as_map().get(idx2, []))  # {feature_idx: weight}

        # intercept (свободный член локальной линейной модели) — приближение base
        intercept = exp.intercept.get(idx2, 0.0) if hasattr(exp, 'intercept') else 0.0

        feats: List[Dict[str, Any]] = []
        for j, col in enumerate(cols):
            feats.append({
                'name': str(col),
                'feature_value': str(row.iloc[0][col]),
                'lime': float(contrib.get(j, 0.0)),
            })
        feats.sort(key=lambda f: abs(f['lime']), reverse=True)
        return {'base': float(intercept), 'features': feats}
    except Exception as e:
        print(f'[lime] _compute_lime failed for {model_name}: {e}')
        return None


