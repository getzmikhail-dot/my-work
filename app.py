# HH.ru: публичный API /vacancies заблокирован (403), но HTML страницы поиска
# отдаются с status=200. Парсим число найденных вакансий регэкспом — это то же
# число, что отображается над списком на самом сайте hh.ru.
_HH_HTML_HEADERS = {
    'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0 Safari/537.36',
    'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
    'Accept-Language': 'ru-RU,ru;q=0.9',
    # Без brotli ('br') — пакет brotli не установлен в venv, иначе requests
    # отдаст сжатый поток и r.text будет нечитаемым.
    'Accept-Encoding': 'gzip, deflate',
    'Referer': 'https://hh.ru/',
}

# Общая сессия для запросов к HH — переиспользует TCP-соединение и cookies,
# что снижает шанс anti-bot блокировки на серии запросов.
_hh_session: Optional[Any] = None


def _get_hh_session():
    global _hh_session
    if _hh_session is None:
        _hh_session = _http.Session()
        _hh_session.headers.update(_HH_HTML_HEADERS)
    return _hh_session

_hh_count_cache: Dict[str, tuple] = {}  # ключ → (timestamp, count)
_HH_COUNT_TTL = 3600.0  # кэш на час, чтобы не дёргать HH при каждой смене формы

# HH регулярно меняет вёрстку, поэтому ищем все числа в формате "N вакансий"
# и берём максимальное — главный счётчик результатов всегда самый большой
# (карточки отдельных вакансий не содержат "N вакансий", только зарплату/опыт).
_HH_NUM_VACANCIES_RE = re.compile(r'(\d[\d\s ]{0,10})\s*ваканс\w*', re.IGNORECASE)


def _count_hh_vacancies(job_title: str, area_id: Optional[str]) -> int:
    """Считает число найденных вакансий через парсинг hh.ru/search/vacancy.
    Возвращает -1 при ошибке, 0 если "вакансий не найдено", иначе фактическое число.
    """
    if not job_title:
        return -1
    key = f'{job_title.lower()}|{area_id or ""}'
    now = time.time()
    cached = _hh_count_cache.get(key)
    if cached and (now - cached[0]) < _HH_COUNT_TTL:
        return cached[1]
    try:
        params = {'text': job_title}
        if area_id:
            params['area'] = area_id
        resp = _get_hh_session().get('https://hh.ru/search/vacancy',
                                     params=params, timeout=12,
                                     allow_redirects=True)
        if resp.status_code != 200:
            return -1
        html = resp.text
        nums = []
        for m in _HH_NUM_VACANCIES_RE.finditer(html):
            digits = re.sub(r'\D', '', m.group(1))
            if digits:
                nums.append(int(digits))
        if nums:
            count = max(nums)
            _hh_count_cache[key] = (now, count)
            return count
        # На странице нет ни одного "N вакансий" — либо реально «ничего не найдено»,
        # либо HH отдал капчу/лендинг. Кэшируем 0 только если явно написано.
        if re.search(r'ничего\s*не\s*найдено|вакансий\s*не\s*найдено', html, re.IGNORECASE):
            _hh_count_cache[key] = (now, 0)
            return 0
        return -1
    except Exception:
        return -1


_hh_areas_cache: Dict[str, str] = {}   # {нормализованное_имя: area_id}
_hh_areas_ts: float = 0.0
_HH_AREAS_TTL = 86400.0  # обновляем раз в сутки


def _nk_hh(s: str) -> str:
    return s.strip().lower().replace('ё', 'е')


def _load_hh_areas() -> Dict[str, str]:
    global _hh_areas_cache, _hh_areas_ts
    if _hh_areas_cache and (time.time() - _hh_areas_ts) < _HH_AREAS_TTL:
        return _hh_areas_cache
    try:
        resp = _http.get(f'{_HH_BASE}/areas', headers=_HH_HEADERS, timeout=8)
        resp.raise_for_status()

        flat: Dict[str, str] = {}

        def traverse(areas: List[Dict]) -> None:
            for a in areas:
                flat[_nk_hh(a['name'])] = str(a['id'])
                if a.get('areas'):
                    traverse(a['areas'])

        traverse(resp.json())
        _hh_areas_cache = flat
        _hh_areas_ts = time.time()
    except Exception as _e:
        print(f'[hh-areas] {_e}')
    return _hh_areas_cache


# Ручные алиасы для популярных разговорных названий городов.
# Подстрочный поиск по HH /areas иначе ловит мелкие посёлки: например,
# «Питер» → «Питерка» (село в Саратовской обл., id=12760) вместо СПб (id=2).
_HH_AREA_ALIASES: Dict[str, str] = {
    'питер': '2', 'спб': '2', 'с-пб': '2', 'с-п': '2', 'питербург': '2',
    'мск': '1',
    'екб': '3', 'екат': '3',
    'нск': '4',  # Новосибирск
}


def _find_area_id(name: str) -> Optional[str]:
    areas = _load_hh_areas()
    key = _nk_hh(name)
    if not key:
        return None
    if key in areas:
        return areas[key]
    if key in _HH_AREA_ALIASES:
        return _HH_AREA_ALIASES[key]
    # Подстрочное совпадение — берём самый длинный совпавший ключ. Но игнорируем
    # совпадения, где ключ из areas короче 5 символов (иначе «итер» из «питер»
    # цепляет какую-нибудь «Питерку»).
    best_id, best_len = None, 0
    for k, v in areas.items():
        if len(k) < 5:
            continue
        if (k in key or key in k) and len(k) > best_len:
            best_len, best_id = len(k), v
    return best_id


def _direction_to_keywords(direction: str) -> str:
    """Строит короткую строку для поискового запроса HH.ru — топ-4 ключевых слова."""
    kws = _get_direction_keywords(direction)
    if kws:
        return ' '.join(kws[:4])
    return re.sub(r'^\d+\.\d+\.\d+\s*', '', direction).strip()


def normalize_cols(df: pd.DataFrame) -> pd.DataFrame:
    mapping = {}
    for c in df.columns:
        low = str(c).strip().lower().replace('ё', 'е')
        if 'уров' in low and 'образ' in low:
            mapping[c] = 'education_level'
        elif 'направ' in low:
            mapping[c] = 'direction'
        elif 'регион' in low:
            mapping[c] = 'region'
        elif 'город' in low:
            mapping[c] = 'city'
        elif 'должност' in low or 'кем вы работаете' in low:
            mapping[c] = 'job'
        elif 'работаете по специальности' in low or 'специальности' in low or 'employed' in low or 'target' in low:
            mapping[c] = 'target'
    return df.rename(columns=mapping)


def normalize_target(v: Any):
    # Числовые значения (в т.ч. '2.0', '1.0', '0.0' после повторного препроцессинга)
    try:
        n = float(str(v).strip())
        if abs(n - 2) < 0.01:
            return 2
        if abs(n - 1) < 0.01:
            return 1
        return 0
    except (ValueError, TypeError):
        pass
    s = str(v).strip().lower().replace('ё', 'е')
    if s in {'да', 'yes', 'true', 'д'} or s.startswith('да'):
        return 2  # трудоустроен по специальности
    if s in {'нет', 'no', 'false', 'н'} or s.startswith('нет'):
        return 1  # трудоустроен не по специальности
    return 0  # не трудоустроен / NaN / неизвестно


def clean_empty(v: Any):
    if v is None:
        return np.nan
    s = str(v).strip()
    if s == '' or s.lower() in {'nan', 'none', 'null', 'na', 'n/a', '-'}:
        return np.nan
    return v


def detect_and_cast_numeric(df: pd.DataFrame, threshold: float = 0.8) -> pd.DataFrame:
    result = df.copy()
    for col in result.columns:
        converted = pd.to_numeric(
            result[col].astype(str).str.replace(',', '.', regex=False),
            errors='coerce'
        )
        original_non_null = result[col].notna().sum()
        ratio = converted.notna().sum() / original_non_null if original_non_null else 0.0
        if ratio >= threshold:
            result[col] = converted
    return result



def _normalize_city_value(v):
    """Нормализация значений колонки city: убирает префиксы "г.", "г. ", "Г. ",
    обрезает пробелы. 'Дубна', 'г. Дубна', 'Г.Дубна' → 'Дубна'."""
    if v is None:
        return v
    s = str(v).strip()
    if not s:
        return v
    # Убираем префиксы города (г., г. , Г., Г. , г ., Г .)
    s2 = re.sub(r'^[гГ]\s*\.\s*', '', s)
    s2 = s2.strip()
    # Возвращаем нормализованное (с заглавной первой буквы для согласованности)
    if s2:
        return s2[0].upper() + s2[1:]
    return s


def _normalize_direction_value(v):
    """Нормализация направления подготовки: убирает суффиксы профиля
    ("_ИСТ (БИС)", "_ИСТ", "(бакалавриат)", "(магистратура)" и т.п.),
    обрезает пробелы. '09.03.02 ИСиТ_ИСТ (БИС)' → '09.03.02 ИСиТ'."""
    if v is None:
        return v
    s = str(v).strip()
    if not s:
        return v
    # Убираем хвост вида "_<суффикс профиля>" — всё начиная с подчёркивания
    s2 = re.sub(r'_[^_]*$', '', s)
    # Убираем хвост вида "(бакалавриат)/(магистратура)/(специалитет)/(...)"
    s2 = re.sub(r'\s*\([^)]*\)\s*$', '', s2)
    return s2.strip()


def _normalize_region_value(v):
    """Нормализация региона: пока что только trim и Заглавная."""
    if v is None:
        return v
    s = str(v).strip()
    if not s:
        return v
    return s[0].upper() + s[1:] if s else s


# Маппинг колонка → функция нормализации значения
_VALUE_NORMALIZERS = {
    'city': _normalize_city_value,
    'direction': _normalize_direction_value,
    'region': _normalize_region_value,
}


def preprocess_dataframe(rows: List[List[Any]]):
    empty_meta = {
        'rows_after_cleaning': 0,
        'original_fill_percent': 0.0,
        'processed_fill_percent': 0.0,
        'filled_missing_count': 0,
        'target_column': None,
        'numeric_columns': [],
        'categorical_columns': [],
    }

    if not rows or len(rows) < 2:
        return rows, empty_meta

    headers = [str(x).strip() for x in rows[0]]
    data = pd.DataFrame(rows[1:], columns=headers)
    total_cells = max(len(data.index) * len(data.columns), 1)

    original_filled_mask = data.notna() & (data.astype(str).apply(lambda col: col.str.strip()) != '')
    original_fill = float(original_filled_mask.sum().sum() / total_cells * 100)

    data = data.apply(lambda col: col.map(clean_empty))
    data = normalize_cols(data)

    # Value-level normalization (city: убираем "г.", direction: убираем суффиксы)
    for col_name, normalizer in _VALUE_NORMALIZERS.items():
        if col_name in data.columns:
            data[col_name] = data[col_name].map(
                lambda v: normalizer(v) if v is not None and not (isinstance(v, float) and pd.isna(v)) else v
            )

    target_col = 'target' if 'target' in data.columns else None
    if target_col:
        data[target_col] = data[target_col].map(normalize_target)

    data = detect_and_cast_numeric(data)

    numeric_cols = data.select_dtypes(include=[np.number]).columns.tolist()
    categorical_cols = [c for c in data.columns if c not in numeric_cols]

    filled_missing = 0

    for col in numeric_cols:
        miss = int(data[col].isna().sum())
        if miss:
            median = data[col].median()
            if pd.isna(median):
                median = 0
            data[col] = data[col].fillna(median)
            filled_missing += miss

    for col in categorical_cols:
        miss = int(data[col].isna().sum())
        if miss:
            data[col] = data[col].fillna('Не указано')
            filled_missing += miss

    processed_fill = float(data.notna().sum().sum() / total_cells * 100)
    out_rows = [data.columns.tolist()] + data.astype(object).where(pd.notna(data), '').values.tolist()

    meta = {
        'rows_after_cleaning': int(len(data)),
        'original_fill_percent': round(original_fill, 1),
        'processed_fill_percent': round(processed_fill, 1),
        'filled_missing_count': int(filled_missing),
        'target_column': target_col,
        'numeric_columns': numeric_cols,
        'categorical_columns': categorical_cols,
    }
    return out_rows, meta


def _load_employment_dataset() -> tuple:
    """Ищет файлы трудоустройства в проекте, прогоняет через ingest и возвращает
    (rows, meta) — тот же формат, что отдаёт /api/ingest. Используется и при
    autoload-обучении, и в /api/demo-employment для фронта.
    """
    import glob, os
    patterns = [
        'Трудоустройство_выпускников_*.xlsx',
        'static/employment/Трудоустройство_выпускников_*.xlsx',
    ]
    paths: List[str] = []
    for pat in patterns:
        paths.extend(glob.glob(pat))
    # Дедуп по имени файла (если один и тот же лежит в нескольких местах)
    seen = set()
    unique_paths: List[str] = []
    for p in paths:
        name = os.path.basename(p)
        if name not in seen:
            seen.add(name)
            unique_paths.append(p)

    dfs: List[pd.DataFrame] = []
    file_metas: List[Dict[str, Any]] = []
    for path in sorted(unique_paths):
        try:
            with open(path, 'rb') as fh:
                data = fh.read()
        except Exception as e:
            file_metas.append({'file': path, 'error': f'read_failed: {e}'})
            continue
        df, meta = ingest_employment_xlsx(data, os.path.basename(path))
        file_metas.append(meta)
        if df is not None and len(df):
            dfs.append(df)

    if not dfs:
        return [], {'files': file_metas, 'error': 'no_employment_files_found'}

    agg = aggregate_employment_dfs(dfs)
    rows = [list(agg.columns)] + agg.astype(object).where(pd.notna(agg), '').values.tolist()
    meta = {
        'files': file_metas,
        'total_rows': int(len(agg)),
        'target_distribution': {int(str(k)): int(v) for k, v in agg['target'].value_counts().to_dict().items()},
        'year_distribution': {int(str(k)): int(v) for k, v in agg['year'].value_counts().to_dict().items()},
        'columns': list(agg.columns),
    }
    return rows, meta


