_CANONICAL_COLS = ['year', 'education_level', 'direction', 'region', 'city', 'organization', 'job', 'target']

_NEG_EMPLOYED = re.compile(r'\bне\s+трудоустро', re.IGNORECASE)
_POS_TOKENS = ('трудоустро', 'работает', 'самозанят', 'фриланс', 'неофициальн', 'стажиров')
# Краткие коды 2025: Р/МР/АР/СЗ — работает; М/А/НР/НД — не работает.
_POS_CODES_EXACT = {'р', 'мр', 'ар', 'сз', 'р (сз)', 'ип'}
_POS_CODE_PREFIX = re.compile(r'^(р|мр|ар|сз)(\b|\s|\(|,)', re.IGNORECASE)


def _classify_employed(v: Any) -> int:
    """Маппит значение колонки 'Статус'/'По окончанию обучения...' в бинарь 0/1.

    Сигнал «по специальности» в данных не закодирован, поэтому target — это
    факт трудоустройства (включая самозанятость/фриланс/стажировку).
    """
    if v is None or (isinstance(v, float) and pd.isna(v)):
        return 0
    s = str(v).strip().lower().replace('ё', 'е')
    if not s or s == '-':
        return 0
    if s in _POS_CODES_EXACT:
        return 1
    if s.startswith('нр'):
        return 0
    if 'трудоустраив' in s:
        return 0
    if _NEG_EMPLOYED.search(s) and 'официальн' not in s:
        return 0
    for t in _POS_TOKENS:
        if t in s:
            return 1
    if _POS_CODE_PREFIX.match(s):
        return 1
    return 0


def _detect_dialect(headers: List[Any]) -> Optional[str]:
    """Определяет год/формат файла по сигнатуре заголовков первого листа."""
    hl = [str(h).strip().lower() for h in headers]
    hset = set(hl)
    if {'выпуск', 'статус', 'направление подготовки'}.issubset(hset):
        return '2021'
    if 'регион трудоустройства' in hset and 'статус' in hset:
        return '2022'
    has_edu_q = any('ваш уровень образования' in h for h in hl)
    has_dir_q = any('по какому направлению вы обучались' in h for h in hl)
    if has_edu_q and has_dir_q:
        if any('защиты в 2023' in h for h in hl):
            return '2023'
        if any('защиты в 2025' in h for h in hl):
            return '2025'
    return None


def ingest_employment_xlsx(file_bytes: bytes, filename: str) -> tuple:
    """Читает один xlsx-файл трудоустройства, нормализует в canonical schema.

    Возвращает (DataFrame|None, meta-dict). Если диалект не распознан —
    DataFrame None и meta содержит error='unknown_dialect'.
    """
    try:
        xl = pd.ExcelFile(_io.BytesIO(file_bytes))
    except Exception as e:
        return None, {'file': filename, 'error': f'read_failed: {e}'}
    sheet = xl.sheet_names[0]
    df = pd.read_excel(_io.BytesIO(file_bytes), sheet_name=sheet)
    dialect = _detect_dialect(list(df.columns))
    if dialect is None:
        return None, {'file': filename, 'sheet': sheet, 'error': 'unknown_dialect',
                      'columns': [str(c) for c in df.columns]}

    mapping = _DIALECT_MAPS[dialect]
    rename: Dict[str, str] = {}
    for col in df.columns:
        target_name = mapping.get(str(col), '_drop')
        if target_name != '_drop':
            rename[col] = target_name
    out = df.rename(columns=rename)
    keep = [c for c in out.columns if c in set(rename.values())]
    out = out[keep]
    # Если два исходных столбца смаппились в один canonical (например, organization
    # с трейлинг-пробелом и без) — оставляем первый.
    if out.columns.duplicated().any():
        out = out.loc[:, ~out.columns.duplicated()]

    # status + job + direction -> target в тернарной схеме:
    #   0 = не трудоустроен (статус — армия/декрет/безработица/«нет инф.»)
    #   1 = трудоустроен НЕ по специальности (работает, но должность не релевантна)
    #   2 = трудоустроен по специальности (должность матчит ключевые слова направления)
    # «По специальности» здесь эвристика через _is_job_relevant — явного признака
    # в файлах ИСАУ нет, но колонка job + словарь ключевых слов по 6-значному коду
    # ФГОС даёт достаточно надёжный сигнал. Тернарная схема нужна фронту, чтобы
    # категория «Не трудоустроены» в блоке «Сравнение когорт» получала свои строки.
    if 'status' in out.columns:
        def _classify_ternary(row: pd.Series) -> int:
            employed = _classify_employed(row.get('status'))
            if employed == 0:
                return 0
            job_val = row.get('job') if 'job' in out.columns else None
            dir_val = row.get('direction') if 'direction' in out.columns else None
            if (job_val is not None and not pd.isna(job_val)
                    and dir_val is not None and not pd.isna(dir_val)
                    and _is_job_relevant(str(job_val), str(dir_val))):
                return 2
            return 1
        out['target'] = out.apply(_classify_ternary, axis=1).astype(int)
    else:
        out['target'] = 0

    out['year'] = int(dialect)

    # Чистим строковые поля от лишних пробелов; пустые строки -> NaN.
    for col in ('education_level', 'direction', 'region', 'city', 'organization', 'job', 'status'):
        if col in out.columns:
            out[col] = out[col].astype(object).where(out[col].notna(), np.nan)
            out[col] = out[col].apply(lambda v: str(v).strip() if pd.notna(v) else np.nan)
            out[col] = out[col].replace({'': np.nan, '-': np.nan})

    # Нормализуем direction: одно направление приходит в трёх форматах между годами:
    #   2021:  "01.03.02  Прикладная математика и информатика"     (двойной пробел)
    #   2022:  "01.03.02  Прикладная математика и информатика(бакалавриат)"
    #   2023:  "01.03.02 Прикладная математика и информатика(бакалавриат)"
    #   2025:  "03.04.02 Физика_(МР)"
    # Без нормализации фронт-комбобокс показывает 2-3 дубля одного направления.
    # Срезаем хвост, начиная с "(" или "_(", и схлопываем множественные пробелы.
    if 'direction' in out.columns:
        def _norm_direction(v: Any) -> Any:
            if pd.isna(v):
                return v
            s = re.sub(r'[\(_].*$', '', str(v))
            s = re.sub(r'\s+', ' ', s).strip()
            return s if s else np.nan
        out['direction'] = out['direction'].apply(_norm_direction)

    status_top: Dict[str, int] = {}
    if 'status' in out.columns:
        vc = out['status'].dropna().astype(str).str.strip().value_counts().head(15)
        status_top = {str(k): int(v) for k, v in vc.to_dict().items()}

    meta = {
        'file': filename,
        'sheet': sheet,
        'dialect': dialect,
        'rows': int(len(out)),
        'target_distribution': {int(str(k)): int(v) for k, v in out['target'].value_counts().to_dict().items()},
        'status_top': status_top,
    }
    return out, meta


_FGOS_CODE_RE = re.compile(r'^(\d{2}\.\d{2}\.\d{2})')


def _canonicalize_directions(s: pd.Series) -> pd.Series:
    """Схлопывает дубликаты направлений по ФГОС-коду: в исходных файлах одно
    направление часто записано с опечатками («Ядерная» vs «Ядерные физика»,
    «электронных» vs «элекиронных»). Для каждого кода XX.XX.XX берём самое
    частое полное название как каноническое.
    """
    nonna = s.dropna().astype(str)
    code_to_canon: Dict[str, str] = {}
    for code, group in nonna.groupby(nonna.str.extract(_FGOS_CODE_RE, expand=False).fillna('')):
        if not code:
            continue
        top = group.value_counts().idxmax()
        code_to_canon[code] = str(top)

    def _map(v: Any) -> Any:
        if pd.isna(v):
            return v
        m = _FGOS_CODE_RE.match(str(v))
        if m and m.group(1) in code_to_canon:
            return code_to_canon[m.group(1)]
        return v
    return s.apply(_map)


# ФГОС: третья цифра в коде XX.YY.ZZ кодирует уровень образования.
# Из 2021 года колонка education_level отсутствует — восстанавливаем её отсюда.
_FGOS_LEVEL_BY_DIGIT = {
    '03': 'Бакалавриат',
    '04': 'Магистратура',
    '05': 'Специалитет',
    '06': 'Аспирантура',
}


def _education_level_from_direction(direction: Any) -> Optional[str]:
    if pd.isna(direction):
        return None
    m = re.match(r'^\d{2}\.(\d{2})\.\d{2}', str(direction))
    if not m:
        return None
    return _FGOS_LEVEL_BY_DIGIT.get(m.group(1))


def aggregate_employment_dfs(dfs: List[pd.DataFrame]) -> pd.DataFrame:
    """Сшивает нормализованные DataFrame'ы в единый canonical-набор."""
    aligned = [df.reindex(columns=_CANONICAL_COLS) for df in dfs]
    out = pd.concat(aligned, ignore_index=True)
    if 'direction' in out.columns:
        out['direction'] = _canonicalize_directions(out['direction'])
    # Восстанавливаем уровень образования из ФГОС-кода для строк, где он пустой
    # (вся 2021-я когорта + редкие NaN в других годах).
    if 'education_level' in out.columns and 'direction' in out.columns:
        missing = out['education_level'].isna() | \
                  out['education_level'].astype(str).str.strip().str.lower().isin(
                      {'', 'nan', 'не указано', 'none', 'null'}
                  )
        recovered = out.loc[missing, 'direction'].apply(_education_level_from_direction)
        out.loc[missing, 'education_level'] = recovered.where(recovered.notna(),
                                                              out.loc[missing, 'education_level'])

        # Нормализация регистра: в данных встречается «магистратура»/«Магистратура»
        # как разные значения. Приводим к канонической форме с заглавной буквы.
        def _norm_edu(v: Any) -> Any:
            if pd.isna(v):
                return v
            s = str(v).strip()
            if not s:
                return np.nan
            low = s.lower().replace('ё', 'е')
            for canon, marker in [('Бакалавриат', 'бакалавр'), ('Магистратура', 'магистр'),
                                  ('Специалитет', 'специал'), ('Аспирантура', 'аспирант')]:
                if marker in low:
                    return canon
            return s
        out['education_level'] = out['education_level'].apply(_norm_edu)
    return out


@app.post('/api/ingest')
async def ingest_employment(files: List[UploadFile] = File(...), _: str = Depends(verify_auth)):
    """Принимает multipart-список xlsx-файлов трудоустройства ИСАУ (разные годы),
    нормализует по сигнатуре шапки и возвращает агрегированный набор строк,
    готовый к подаче в /api/preprocess или /api/train.
    """
    dfs: List[pd.DataFrame] = []
    file_metas: List[Dict[str, Any]] = []
    for f in files:
        data = await f.read()
        df, meta = ingest_employment_xlsx(data, f.filename or '<unnamed>')
        file_metas.append(meta)
        if df is not None and len(df):
            dfs.append(df)

    if not dfs:
        return {'rows': [], 'meta': {'files': file_metas, 'error': 'no_recognized_files'}}

    agg = aggregate_employment_dfs(dfs)
    rows = [list(agg.columns)] + agg.astype(object).where(pd.notna(agg), '').values.tolist()
    return {
        'rows': rows,
        'meta': {
            'files': file_metas,
            'total_rows': int(len(agg)),
            'target_distribution': {int(str(k)): int(v) for k, v in agg['target'].value_counts().to_dict().items()},
            'year_distribution': {int(str(k)): int(v) for k, v in agg['year'].value_counts().to_dict().items()},
            'columns': list(agg.columns),
        },
    }


