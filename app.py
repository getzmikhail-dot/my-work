def verify_auth(credentials: HTTPBasicCredentials = Depends(_security)) -> str:
    """Проверка Basic Auth. compare_digest защищает от тайминговых атак."""
    user_ok = _secrets.compare_digest(credentials.username, _AUTH_USER)
    pass_ok = _secrets.compare_digest(credentials.password, _AUTH_PASS)
    if not (user_ok and pass_ok):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail='Неверный логин или пароль',
            headers={'WWW-Authenticate': 'Basic realm="Employment Lab"'},
        )
    return credentials.username


